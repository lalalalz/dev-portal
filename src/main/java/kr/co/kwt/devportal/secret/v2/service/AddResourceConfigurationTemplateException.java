package kr.co.kwt.devportal.secret.v2.service;

public class AddResourceConfigurationTemplateException extends RuntimeException {
    public AddResourceConfigurationTemplateException() {
    }

    public AddResourceConfigurationTemplateException(String message) {
        super(message);
    }

    public AddResourceConfigurationTemplateException(String message, Throwable cause) {
        super(message, cause);
    }

    public AddResourceConfigurationTemplateException(Throwable cause) {
        super(cause);
    }

    public AddResourceConfigurationTemplateException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
